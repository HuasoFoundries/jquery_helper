function hammerify(el, options) {
	var $el = $(el);
	if (!$el.data("hammer")) {
		$el.data("hammer", new Hammer($el[0], options));
	}
}

$.fn.stop = function () {
	return this.each(function () {
		$(this).velocity('stop');
	});
};

$.fn.hammer = function (options) {
	return this.each(function () {
		hammerify(this, options);
	});
};

